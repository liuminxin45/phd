/**
 * Prompt builders for the AI blog generation pipeline.
 * Each function returns a user-facing prompt string for a specific LLM step.
 */

export function buildAnalysisPrompt(postSummaries: string, topBodies: string, postCount: number): string {
  return `你是一位资深技术博客分析专家。以下是一个公司内部技术博客平台上"最多赞"的前 ${postCount} 篇技术博客的信息：

${postSummaries}

以下是其中点赞最高的 5 篇博客的正文节选（用于更深入分析写作风格和排版）：

${topBodies}

请你分析这些热门技术博客的共性，找出它们受欢迎的关键因素。

**重要：请只关注与技术写作本身相关的因素（选题方向、写作风格、排版结构、技术深度等），尽可能忽略与具体公司项目/业务相关的内容。**

请从以下维度进行分析：
1. **选题特征**：这些热门博客通常选择什么类型的技术话题？有什么规律？
2. **内容结构**：它们的排版、段落组织、标题层次有什么共同模式？平均字数大概多少？
3. **写作风格**：语言风格、叙述方式（教程式/叙事式/对比式等）有什么特点？
4. **技术深度**：技术内容的深浅程度如何？理论与实践的比例？
5. **实用价值**：它们如何体现实用性？是否有代码示例、架构图等？
6. **关键成功因素总结**：提炼出 3-5 个最关键的成功因素

请给出结构化的分析结果。`;
}

export function buildOutlinePrompt(analysis: string): string {
  return `基于以下对热门技术博客的分析结果，请你自主拟定一篇全新的技术博客。

## 分析结果

${analysis}

## 要求

1. **选定一个技术话题**：根据分析出的受欢迎选题特征，选择一个当前热门且实用的技术话题（不要与具体公司项目相关，应是通用技术话题）
2. **拟定标题**：模仿热门博客的标题风格，吸引眼球
3. **设计详细大纲**：
   - 给出完整的章节结构（使用 Phabricator Remarkup 的 = 标题 = 格式说明层级）
   - 每个章节的核心内容概要（2-3 句话说明要写什么）
   - 预估每个章节的大致字数
4. **规划图片位置**：标注哪些位置需要插入图片/架构图，并说明图片应展示的内容
5. **目标字数**：参考热门博客的平均篇幅，确定目标总字数（建议 2000-4000 字）

请严格按照以下格式输出：

标题: [你拟定的博客标题]
目标字数: [数字]

---

[详细大纲内容]`;
}

export function buildWritePrompt(outline: string): string {
  return `请根据以下大纲，撰写一篇完整的技术博客正文。

## 大纲

${outline}

## 格式要求（Phabricator Remarkup）

1. **标题层级**：
   - 一级标题用 \`= 标题 =\`
   - 二级标题用 \`== 标题 ==\`
   - 三级标题用 \`=== 标题 ===\`

2. **富文本**：
   - 粗体用 \`**粗体**\`
   - 代码块用三个反引号包裹并标注语言
   - 行内代码用单个反引号
   - 无序列表用 \`- 项目\`
   - 有序列表用 \`# 项目\`
   - 表格用 \`| 列1 | 列2 |\` 格式
   - 引用用 \`> 引用内容\`

3. **写作风格**：
   - 模拟热门技术博客的口吻：技术性强但通俗易懂
   - 适当使用代码示例来说明概念
   - 段落之间保持逻辑连贯，有承上启下的过渡

4. **图片占位**：
   - 在需要插入图片/架构图的位置，使用以下格式预留：
     \`{F????, size=full}\`
   - 紧跟一行注释说明需要贴什么图，格式：\`// 图片说明：[描述需要贴什么样的图片]\`
   - 至少预留 2-3 处图片位置

5. **字数**：请按照大纲中的目标字数撰写，内容要充实

请直接输出博客正文（Remarkup 格式），不要输出任何额外说明或前言。`;
}
